#define TIMER_GLOBAL
#include "include.h"
////////////////////////////////////////////////////////////////////////////////
// 功能描述 : 定时器初始化
// 输入参数 : 
// 返回参数 : 
// 说明     : 2ms
////////////////////////////////////////////////////////////////////////////////
void InitT0(void)
{
	AUXR &= 0x7F;		//定时器时钟12T模式
	TMOD &= 0xF0;		//设置定时器模式
	TMOD |= 0x01;		//设置定时器模式
	TL0 = 0xcc;		  //设置定时初值
	TH0 = 0xF8;		  //设置定时初值
	TF0 = 0;		    //清除TF0标志
	TR0 = 1;		    //定时器0开始计时
	ET0  = 1;       //allow interrupt
}
